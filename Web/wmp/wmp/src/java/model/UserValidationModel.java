package model;

import obj.*;

/**
 *
 * @author marco
 */
public class UserValidationModel {
    private int id;
    private int idPhoneInfo;
    private int number;
    private boolean validated;
    private PhoneInfoModel phoneInfo;

    public UserValidationModel(int id, int idPhoneInfo, int number, boolean validated, PhoneInfoModel phoneInfo) {
        this.id = id;
        this.idPhoneInfo = idPhoneInfo;
        this.number = number;
        this.validated = validated;
        this.phoneInfo = phoneInfo;
    }

    public UserValidationModel() {
    }
        
        
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdPhoneInfo() {
        return idPhoneInfo;
    }

    public void setIdPhoneInfo(int idPhoneInfo) {
        this.idPhoneInfo = idPhoneInfo;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public boolean isValidated() {
        return validated;
    }

    public void setValidated(boolean validated) {
        this.validated = validated;
    }    

    public PhoneInfoModel getPhoneInfo() {
        return phoneInfo;
    }

    public void setPhoneInfo(PhoneInfoModel phoneInfo) {
        this.phoneInfo = phoneInfo;
    } 
}
